function shuffle(array) {
  return array.sort(() => Math.random() - 0.5);
}

let quizWords = [];
let current = 0;
let score = 0;

function loadListButtons() {
  const container = document.getElementById("list-buttons");
  container.innerHTML = "";
  let i = 1;
  for (let listName in lists) {
    const btn = document.createElement("button");
    btn.textContent = `List ${i}`;
    btn.className = "list-btn";
    btn.onclick = () => startQuiz(listName);
    container.appendChild(btn);
    i++;
  }
}

function startQuiz(listName) {
  quizWords = shuffle([...lists[listName]]);
  current = 0;
  score = 0;

  document.getElementById("list-screen").style.display = "none";
  document.getElementById("quiz-screen").style.display = "block";
  newQuestion();
}

function goHome() {
  document.getElementById("quiz-screen").style.display = "none";
  document.getElementById("list-screen").style.display = "block";
}

function newQuestion() {
  const questionDiv = document.getElementById("question");
  const optionsDiv = document.getElementById("options");
  const resultDiv = document.getElementById("result");
  const bar = document.getElementById("bar");

  resultDiv.textContent = "";

  if (current >= quizWords.length) {
    const percentage = Math.round((score / quizWords.length) * 100);
    questionDiv.textContent = "ðŸŽ‰ Quiz bitti!";
    optionsDiv.innerHTML = "";
    resultDiv.innerHTML = `âœ… DoÄŸru: ${score} / ${quizWords.length} <br> ðŸ“Š BaÅŸarÄ±: %${percentage}`;
    bar.style.width = "100%";
    return;
  }

  const word = quizWords[current];
  questionDiv.textContent = `"${word.en}"`;

  let options = [word.tr];
  while (options.length < 4) {
    let wrong = quizWords[Math.floor(Math.random() * quizWords.length)].tr;
    if (!options.includes(wrong)) options.push(wrong);
  }

  options = shuffle(options);

  optionsDiv.innerHTML = "";
  options.forEach(opt => {
    const btn = document.createElement("button");
    btn.textContent = opt;
    btn.className = "option";
    btn.onclick = () => {
      if (opt === word.tr) {
        resultDiv.textContent = "âœ… Correct!";
        score++;
      } else {
        resultDiv.textContent = "âŒ Wrong!";
      }
      current++;
      bar.style.width = ((current / quizWords.length) * 100) + "%";
      setTimeout(newQuestion, 800);
    };
    optionsDiv.appendChild(btn);
  });
}

function checkAnswer(selected) {
  const buttons = document.querySelectorAll("#options button");

  if (selected === currentQuestion.answer) {
    buttons.forEach(btn => {
      if (btn.textContent === selected) {
        btn.classList.add("correct");
      }
    });
    document.getElementById("result").textContent = "Correct âœ…";
  } else {
    buttons.forEach(btn => {
      if (btn.textContent === selected) {
        btn.classList.add("wrong"); 
      }
      if (btn.textContent === currentQuestion.answer) {
        btn.classList.add("correct");
      }
    });
    document.getElementById("result").textContent = "Wrong âŒ";
  }

  buttons.forEach(btn => btn.disabled = true);

  setTimeout(nextQuestion, 1000); 
}

function showQuestion() {
  const optionsDiv = document.getElementById("options");
  optionsDiv.innerHTML = "";

  currentQuestion.options.forEach(option => {
    const button = document.createElement("button");
    button.textContent = option;
    button.onclick = () => checkAnswer(option);
    optionsDiv.appendChild(button);
  });

  document.getElementById("result").textContent = "";
}

